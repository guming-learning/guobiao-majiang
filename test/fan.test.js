let pass = 0, fail = 0;
const failMsgs = [];

function eqArr(a, b) {
  if (a.length !== b.length) return false;
  for (let i = 0; i < a.length; i++) if (a[i] !== b[i]) return false;
  return true;
}

// 字符串解析 + 还原测试
function strTest(ret, s, correct = '', flagCheck = true) {
  const ht = new Handtiles();
  const r = ht.StringToHandtiles(s);
  if (r !== ret) { fail++; failMsgs.push(`[str] "${s}" ret=${r} 期望 ${ret}`); return; }
  if (ret === 0) {
    const outs = ht.HandtilesToString();
    const c = correct || s.replace(/ /g, '');
    if (flagCheck) {
      if (outs === c || outs === c + '|EE0000|' || outs === c + '|') { pass++; }
      else { fail++; failMsgs.push(`[str] "${s}" 还原=${outs} 期望 ${c}`); }
    } else { pass++; }
  } else { pass++; }
}

// 听牌测试
function tingTest(s, tiles) {
  const ht = new Handtiles();
  const r = ht.StringToHandtiles(s);
  if (r !== 0) { fail++; failMsgs.push(`[ting] "${s}" 解析失败 ret=${r}`); return; }
  const fan = new Fan();
  const got = fan.CalcTing(ht).map((t) => t.GetId()).sort((a, b) => a - b);
  const exp = tiles.slice().sort((a, b) => a - b);
  if (eqArr(got, exp)) pass++;
  else { fail++; failMsgs.push(`[ting] "${s}" 得到 [${got}] 期望 [${exp}]`); }
}

// 算番测试
function fanTest(s, v, v2 = null) {
  const ht = new Handtiles();
  const r = ht.StringToHandtiles(s);
  if (r !== 0) { fail++; failMsgs.push(`[fan] "${s}" 解析失败 ret=${r}`); return; }
  const fan = new Fan();
  fan.CountFan(ht);
  const res = [];
  for (let i = 1; i < FAN.FAN_SIZE; i++) {
    for (let j = 0; j < fan.fan_table_res[i].length; j++) res.push(i);
  }
  const sres = res.slice().sort((a, b) => a - b);
  const sv = v.slice().sort((a, b) => a - b);
  const ok = eqArr(sres, sv) || (v2 && eqArr(sres, v2.slice().sort((a, b) => a - b)));
  if (ok) pass++;
  else {
    fail++;
    failMsgs.push(`[fan] "${s}" 番数=${fan.tot_fan_res}\n     得到: ${sres.map((i) => FAN_NAME[i]).join(' ')}\n     期望: ${sv.map((i) => FAN_NAME[i]).join(' ')}`);
  }
}

